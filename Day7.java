
package day7;


public class Day7 {

    
    public static void main(String[] args) {
        
        System.out.println("penugasan: ");
        int x=12;
        int y=4;
        int z=3;
        
        //penjumlahan
        x+=y;
        System.out.println("hasil penjumlahan: "+x);
        
        //pengurangan
        x-=z;
        System.out.println("hasil pengurangan: "+x);
        
        //perkalian
        x*=y;
        System.out.println("hasil perkalian: "+x);
        
        //pembagian
        x/=y;
        System.out.println("hasil penjumlahan: "+x);
        
        //modulus
        x%=z;
        System.out.println("sisa bagi: "+x);
               
    }
    
}
